# 1. if statement
x = 10
if x > 5:
    print("X is greater than 5")

# 2. if-else statement
age = 16
if age >= 18:
    print("You can vote")
else:
    print("You cannot vote")

# 3. if-elif-else statement
marks = int(input("Enter your marks : "))
if marks >= 90:
    print("Grade: A+")
elif marks >= 75:
    print("Grade: A")
elif marks >= 60:
    print("Grade: B")
else:
    print("Grade: C")    
